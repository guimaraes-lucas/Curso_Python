#  + = Adição          ** ( pow() )= Potência
#  - = Subtração       // = Divisão Inteira
#  * = Multiplicação   %  = Resto da Divisão
#  / = Divisão

#   ============Exemplos================

#   5 + 2 == 7          5 ** 2  == 25 / pow(5,2)
#   5 - 2 == 3          5 // 2  == 2
#   5 * 2 == 10         5 % 2   == 1
#   5 / 2 == 2.5

#   =============Ordem de Precedência================

#   1 = ()
#   2 = **
#   3 = * / // %
#   4 = + -
