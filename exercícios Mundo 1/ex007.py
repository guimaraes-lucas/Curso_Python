# Desenvolva um programa que leia as notas de um aluno, calcule e mostre a sua média

x1 = float(input('Digite a nota do aluno: '))
x2 = float(input('Digite a nota do aluno: '))
x3 = float(input('Digite a nota do aluno: '))
x4 = float(input('Digite a nota do aluno: '))

m = (x1 + x2 + x3 + x4) / 4

print('A média do aluno é: {}'.format(m))

